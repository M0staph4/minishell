#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*s1;

	s1 = (char *)s;
	i = ft_strlen(s1);
	while (i >= 0)
	{
		if (s1[i] == (char)c)
		{
			s1 += i;
			return (s1);
		}
		i--;
	}
	return (0);
}
