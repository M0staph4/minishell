#include "libft.h"

int	ft_atoi(const char *str)
{
	int	x;
	int	y;
	int	z;

	x = 0;
	y = 0;
	z = 1;
	while (str [x] && (str [x] == '\n' || str [x] == '\t' || str[x] == '\r'
			|| str[x] == '\f' || str [x] == '\v' || str[x] == ' '))
		x++;
	if ((str[x] == '-' || str[x] == '+'))
	{
		if (str[x] == '-')
			z = -z;
		x++;
	}
	while (str[x] && (str[x] >= '0' && str[x] <= '9'))
	{
		y = y * 10 + str[x] - '0';
		x++;
	}
	return (z * y);
}
