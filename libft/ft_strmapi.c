#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int		i;
	char				*str;
	size_t				len;
	char				*s1;

	if (!s)
		return (NULL);
	s1 = (char *)s;
	len = ft_strlen(s1);
	i = 0;
	str = malloc(sizeof(char) * len + 1);
	if (!str)
		return (0);
	while (s[i])
	{
		str[i] = f(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
